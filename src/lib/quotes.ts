// Retourne la phrase du jour, tournant chaque 24h
export function getPhraseduJour(): string {
  const start = new Date(new Date().getFullYear(), 0, 0);
  const now = new Date();
  const diff = now.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  return PHRASES_EQUIPE[dayOfYear % PHRASES_EQUIPE.length];
}
